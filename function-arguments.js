function math(argOne, argTwo, argThree) {
  // the function math consists of 3 arguments //
  return argTwo * argThree + argOne; // the output of multiplying the 2nd and 3rd argument, followed by adding that sum to the 1st argument, is returned from this function //
}
console.log(math(53, 61, 67)); // this prints the result of 61 * 67 + 53 //
